package com.hong_studio.numberbaseballgame;

public class RecyclerItem {
    String num1;
    String num2;
    String num3;
    String strike;
    String ball;

    public RecyclerItem() {
    }

    public RecyclerItem(String num1, String num2, String num3, String strike, String ball) {
        this.num1 = num1;
        this.num2 = num2;
        this.num3 = num3;
        this.strike = strike;
        this.ball = ball;
    }
}
